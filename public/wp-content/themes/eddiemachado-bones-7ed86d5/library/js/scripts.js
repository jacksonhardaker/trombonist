{
  var _fixedHeader = false;
  var _fixedHeaderThreshold = null;
  var _header = $($("nav")[0]);
  
  $(document).ready(function(){
    _fixedHeaderThreshold = _header.offset().top;
    $($("#mobile-menu").children()[0]).click(function(){$($("nav")[0]).toggleClass("show"); });
    $(window).scroll(function(){
      var top = $(window).scrollTop();
      if (top >= _fixedHeaderThreshold && !_fixedHeader)
      {
        _fixedHeader = true;
        _header.addClass("fixed");
        $(".top-nav").addClass("wrap");
        $("#stationary-menu").addClass("wrap transition boxShadowDefault");
        $($("header")[0]).addClass("fixedmenu");
        $("#flyin-header").addClass("transition");
        setTimeout(function() {
          $(".top-nav").addClass("");
          $("#stationary-menu").addClass("fullwidth");
          setTimeout(function() {$("#flyin-header").addClass("show");}, 30);
      }, 10);
      }
      else if (top < _fixedHeaderThreshold && _fixedHeader)
      {
        _fixedHeader = false;
        $("#flyin-header").removeClass("transition");
        //$(".top-nav").removeClass("");
        $("#stationary-menu").removeClass("fullwidth");
        $("#flyin-header").removeClass("show");
        _header.removeClass("fixed");
        $($("header")[0]).removeClass("fixedmenu");
        $(".top-nav").removeClass("wrap");
        $("#stationary-menu").removeClass("wrap transition boxShadowDefault");
      }
    });
  });
}
